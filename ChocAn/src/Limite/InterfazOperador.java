    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
            this.jRadioButtonMiembro.setSelected(false);
            this.jRadioButtonProveedor.setSelected(false);
        try{
            this.controlChocan.llenarLista(this.modelo);
            this.controlChocan.guardarArchivo("Miembro");
            this.controlChocan.guardarArchivo("Proveedor");
            this.vaciartabla();
            this.llenarTabla();
            this.vaciartabla();
            modelo.addRow(controlChocan.buscar(Integer.valueOf(this.jTextField1.getText())));
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error Numero no Existe", "ERROR", WIDTH);
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    public void insertar(){
    
        if(this.jTextFieldNombre.getText().length() > 2 && this.jTextFieldEmail.getText().length() > 6){
            if(this.jComboBox1.getSelectedItem().toString().equalsIgnoreCase("Miembro")){
                ChocAn.listaMiembro.add(new Miembro(this.controlChocan.codigo(this.jComboBox1.getSelectedItem().toString()),
                        this.jTextFieldNombre.getText(),
                        this.jTextFieldEmail.getText(),
                        this.jTextFieldDireccion.getText(),
                        this.jTextFieldCiudad.getText(),Integer.valueOf(this.jTextFieldZip.getText()),"Activado")
                );
            }else if(this.jComboBox1.getSelectedItem().toString().equalsIgnoreCase("Proveedor")){
                ChocAn.listaProveedor.add(new Proveedor(this.controlChocan.codigo(this.jComboBox1.getSelectedItem().toString()),
                        this.jTextFieldNombre.getText(),this.jTextFieldEmail.getText(),
                        this.jTextFieldDireccion.getText(),
                        this.jTextFieldCiudad.getText(),Integer.valueOf(this.jTextFieldZip.getText()))
                );
            }
        }
    }
    
