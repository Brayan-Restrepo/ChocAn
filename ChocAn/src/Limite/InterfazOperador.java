    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Esta Seguro que desea Eliminar el registro", "Confirmar",JOptionPane.YES_NO_OPTION)==0)
        {
          try{  
            this.controlChocan.eliminarCodigo(Integer.valueOf(this.modelo.getValueAt(this.jTable1.getSelectedRow(), 0).toString()));
            this.controlChocan.llenarLista(this.modelo);
            this.controlChocan.guardarArchivo("Miembro");
            this.controlChocan.guardarArchivo("Proveedor");
            this.vaciartabla();
            this.llenarTabla();
          }catch(Exception e ){
              JOptionPane.showMessageDialog(this, "Debe de seleccionar un registro", "ERROR", WIDTH);
          }
        
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

