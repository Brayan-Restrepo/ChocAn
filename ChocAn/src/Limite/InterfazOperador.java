    public void llenarTabla(){
        this.vaciartabla();
        
        if(this.jRadioButtonMiembro.isSelected()==true){
            for (int i = 0; i < ChocAn.listaMiembro.size(); i++) {
                String[] fila={
                    String.valueOf(ChocAn.listaMiembro.get(i).getNumero()),
                    ChocAn.listaMiembro.get(i).getNombre(),
                    ChocAn.listaMiembro.get(i).getEmail(),
                    String.valueOf(ChocAn.listaMiembro.get(i).getEstado()),
                    ChocAn.listaMiembro.get(i).getDireccion(),
                    ChocAn.listaMiembro.get(i).getCiudad(),
                    String.valueOf(ChocAn.listaMiembro.get(i).getZip())
                };
                modelo.addRow(fila);
            }
        }
        
        if(this.jRadioButtonProveedor.isSelected()==true){
            for (int i = 0; i < ChocAn.listaProveedor.size(); i++) {
                String[] fila={
                    String.valueOf(ChocAn.listaProveedor.get(i).getNumero()),
                    ChocAn.listaProveedor.get(i).getNombre(),
                    ChocAn.listaProveedor.get(i).getEmail(),
                    "",
                    ChocAn.listaProveedor.get(i).getDireccion(),
                    ChocAn.listaProveedor.get(i).getCiudad(),
                    String.valueOf(ChocAn.listaProveedor.get(i).getZip())
                };
                modelo.addRow(fila);
            }
        }
    }
    
